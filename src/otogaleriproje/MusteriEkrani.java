    public void click()
    {
      tblArac.addMouseListener(new java.awt.event.MouseAdapter() {
    @Override
    public void mouseClicked(java.awt.event.MouseEvent evt) {
        int row = tblArac.rowAtPoint(evt.getPoint());
        
        String seciliKS = model.getValueAt(row, 5).toString();
        if (seciliKS.equals("Satıldı") ) {
            btnSatinAl.setEnabled(false);
        }
        else
        {
         btnSatinAl.setEnabled(true);
        }
        if (seciliKS.equals("Kiralandı")) {
            btnKirala.setEnabled(false);
        }
        else
        {
         btnKirala.setEnabled(true);
        }
    }
});
    
    }
    
           public void aracGoruntule()
    {
        model.setRowCount(0);
        ArrayList<AracBilgi> araclar = new ArrayList<AracBilgi>();
        araclar = islemler.aracListele();
        
        if (araclar!=null) {
            for(AracBilgi item : araclar)
            {
             Object[] eklenecek = {item.getArac_Plaka(),item.getArac_Marka(),item.getArac_Model(),item.getArac_Tipi(),item.getArac_YakitTip(),item.getArac_KiraSat(),item.getArac_Fiyat(),item.getArac_MotorHacmi()};
             model.addRow(eklenecek);
            }
        }
        
    }
           
       public void aracGoruntule2(String marka,String aracTip,String yakitTip,String alimTip,String minMotorHacmi,String maxMotorHacmi,String minFiyat,String maxFiyat)
    {
        model.setRowCount(0);
        
        ArrayList<AracBilgi> araclar = new ArrayList<AracBilgi>();
        araclar = islemler.aracFiltrele(marka,aracTip,yakitTip,alimTip,minMotorHacmi,maxMotorHacmi,minFiyat,maxFiyat);
        
        if (araclar!=null) {
            for(AracBilgi item : araclar)
            {
             Object[] eklenecek = {item.getArac_Plaka(),item.getArac_Marka(),item.getArac_Model(),item.getArac_Tipi(),item.getArac_YakitTip(),item.getArac_KiraSat(),item.getArac_Fiyat(),item.getArac_MotorHacmi()};
             model.addRow(eklenecek);
            }
        }
        
    }
             
             
                public void personelLabel(int id)
    {
        
        
        ArrayList<PersonelBilgi> araclar = new ArrayList<PersonelBilgi>();
        araclar = islemler.personelListele(id);
        
        if (araclar!=null) {
            for(PersonelBilgi item : araclar)
            {
             
             lblPersonel.setText("İlgili Personeliniz : " +item.getPersonel_Adi());
            }
        }
        
    }
    
    
        
    
    
    
    
