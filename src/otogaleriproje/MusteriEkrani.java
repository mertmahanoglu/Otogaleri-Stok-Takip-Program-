    private void btnFiltreleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltreleActionPerformed
        
        String marka = cmbMarka.getSelectedItem().toString();
        String aracTip = cmbAracTipi.getSelectedItem().toString();
        String yakitTip = cmbYakit.getSelectedItem().toString();
        String alimTip = "";
        String minFiyat = tbFiyatAz.getText();
        String maxFiyat = tbFiyatCok.getText();
        String minMotorHacmi = tbMotorAz.getText();
        String maxMotorHacmi = tbMotorCok.getText();
        int rowcount =tblArac.getRowCount();
        if (rowcount==0) {

                JOptionPane.showMessageDialog(this, "Filtrelediğiniz Biçimde Araç Bulunmamaktadır");
            }
            
        if (cmbAlim.getSelectedItem().toString()=="S(Satılık)") {
        alimTip = "S";
        }
        else
        {
         alimTip = "K";
        }
        
        if (tbFiyatAz.getText().isEmpty() || tbFiyatCok.getText().isEmpty()|| tbMotorAz.getText().isEmpty() || tbMotorCok.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Alanlar Boş Bırakılamaz. Lütfen yeni bir filtre seçin");
   
        }
        else
        {
               
    aracGoruntule2(marka,aracTip,yakitTip,alimTip,minMotorHacmi,maxMotorHacmi,minFiyat,maxFiyat);
    
        

            

        
        
    btnFiltrele.setEnabled(false);
    lblUyari.setVisible(true);
        }
        
       
       

        
        
 
    
        
    }//GEN-LAST:event_btnFiltreleActionPerformed

