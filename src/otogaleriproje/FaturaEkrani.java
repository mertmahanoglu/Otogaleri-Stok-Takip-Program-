    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
      
         int faturaNo = Integer.parseInt(tbFaturaNo.toString());
        String Alici = txtfAlici.toString();
        String plaka = tbPlaka.toString();
        int personelID = Integer.parseInt(tbPersonel.toString());
        int fiyat = Integer.parseInt(tbFiyat.toString());
        int galeriID = Integer.parseInt(tbGaleri.toString());
        
        
        islemler.faturaGuncelle(faturaNo, Alici, plaka, galeriID, fiyat, personelID);
    }//GEN-LAST:event_btnUpdateActionPerformed

