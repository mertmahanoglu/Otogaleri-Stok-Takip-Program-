           public void faturaGoruntule()
    {
        model.setRowCount(0);
        ArrayList<FaturaBilgi> faturalar = new ArrayList<FaturaBilgi>();
        faturalar = islemler.faturaListele();
        
        if (faturalar!=null) {
            for(FaturaBilgi item : faturalar)
            {
             Object[] eklenecek = {item.getFatura_no(),item.getFatura_alici(),item.getFatura_Alici_TC(),item.getFatura_plaka(),item.getFatura_personel(),item.getFatura_fiyat(),item.getFatura_KS()};
             model.addRow(eklenecek);
            }
        }
        
    } 
    
    
